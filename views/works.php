<script>
    document.addEventListener('DOMContentLoaded', () => {
        const cards = document.querySelectorAll('.card');
        const cardsPerPageDesktop = 5;
        const cardsPerPageMobile = 2;
        let currentPage = 1;

        function showPage(page, isMobile) {
            const cardsPerPage = isMobile ? cardsPerPageMobile : cardsPerPageDesktop;
            const start = (page - 1) * cardsPerPage;
            const end = start + cardsPerPage;

            cards.forEach((card, index) => {
                card.style.display = (index >= start && index < end) ? 'block' : 'none';
            });
        }

        function setupPagination() {
            const numPages = Math.ceil(cards.length / cardsPerPageDesktop);
            const prevButton = document.querySelector('.prev');
            const nextButton = document.querySelector('.next');

            function handleResize() {
                const isMobile = window.innerWidth <= 1300;
                const numPages = Math.ceil(cards.length / (isMobile ? cardsPerPageMobile : cardsPerPageDesktop));

                prevButton.addEventListener('click', () => {
                    if (currentPage > 1) {
                        currentPage--;
                        showPage(currentPage, isMobile);
                    }
                });

                nextButton.addEventListener('click', () => {
                    if (currentPage < numPages) {
                        currentPage++;
                        showPage(currentPage, isMobile);
                    }
                });


                showPage(currentPage, isMobile);
            }

            window.addEventListener('resize', handleResize);
            handleResize();
        }

        setupPagination();
    });
</script>